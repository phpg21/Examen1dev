package com.udem.examen.abstracts;

import com.udem.examen.interfaces.Reino;

/**
 *
 * @author b12s309
 */
public abstract class Plantas implements Reino{
   public String nombre;
   public String color;
   public int edad;

    public Plantas(String nombre, String color, int edad) {
        this.nombre = nombre;
        this.color = color;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    
    
}
